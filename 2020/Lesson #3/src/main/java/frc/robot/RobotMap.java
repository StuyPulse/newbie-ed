package frc.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
  public static final int DRIVETRAIN_LEFT_FRONT_MOTOR_PORT = -1;
  public static final int DRIVETRAIN_RIGHT_FRONT_MOTOR_PORT = -1;
  public static final int DRIVETRAIN_LEFT_REAR_MOTOR_PORT = -1;
  public static final int DRIVETRAIN_RIGHT_REAR_MOTOR_PORT = -1;

  public static final int FLOOP_SOLENOID_PORT = -1;
  public static final int PUSHER_SOLENOID_PORT = -1;

}
